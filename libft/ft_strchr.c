#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*str;
	char	ch;

	str = (char *)s;
	ch = (char)c;
	while (*str != '\0' && *str != ch)
		str++;
	if (*str != ch)
		return (NULL);
	return (str);
}
